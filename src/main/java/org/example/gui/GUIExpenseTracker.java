    private void buttonSaveExpenseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonSaveExpenseActionPerformed
        try {
        double amount = Double.parseDouble(txtNominalExpense.getText());
        String deskripsi = txtKetExpense.getText();
        String kategori = (String) comboKategoriExpense.getSelectedItem(); // pastikan kombo ini ada
        LocalDate date = dateChooserExpense.getDate().toInstant()
            .atZone(ZoneId.systemDefault()).toLocalDate();

        int jam = (int) spinnerJamExpense.getValue();
        int menit = (int) spinnerMenitExpense.getValue();
        LocalDateTime tanggal = date.atTime(jam, menit);

        Expense expense = new Expense(nextId++, amount, deskripsi, kategori, tanggal);
        report.tambahTransaksi(expense);

        JOptionPane.showMessageDialog(this, "Expense berhasil disimpan!");

        // Reset form
        txtNominalExpense.setText("");
        txtKetExpense.setText("");
        comboKategoriExpense.setSelectedIndex(0);
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Nominal harus berupa angka.");
    }
    }//GEN-LAST:event_buttonSaveExpenseActionPerformed

