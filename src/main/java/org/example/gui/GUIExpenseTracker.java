    private void buttonSaveIncomeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonSaveIncomeActionPerformed
        try {
        double amount = Double.parseDouble(txtNominalIncome.getText());
        String deskripsi = txtKetIncome.getText();
        String kategori = (String) comboKategoriIncome.getSelectedItem(); // pastikan kombo ini ada
        LocalDateTime tanggal = LocalDateTime.now();

        Income income = new Income(nextId++, amount, deskripsi, kategori, tanggal);
        report.tambahTransaksi(income);

        JOptionPane.showMessageDialog(this, "Income berhasil disimpan!");

        // Reset form
        txtNominalIncome.setText("");
        txtKetIncome.setText("");
        comboKategoriIncome.setSelectedIndex(0);
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Nominal harus berupa angka.");
    }
    }//GEN-LAST:event_buttonSaveIncomeActionPerformed

